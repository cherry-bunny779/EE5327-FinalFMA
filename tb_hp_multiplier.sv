////////////////////////////////////////////////////////////////////////////////
// Testbench: tb_hp_multiplier
// Description: Testbench for IEEE 754 Half-Precision Floating-Point Multiplier
////////////////////////////////////////////////////////////////////////////////

`timescale 1ns / 1ps

module tb_hp_multiplier;

    //==========================================================================
    // Signals
    //==========================================================================
    
    logic [15:0] a;
    logic [15:0] b;
    logic [15:0] result;
    logic        overflow;
    logic        underflow;
    logic        nan_out;
    
    //==========================================================================
    // DUT Instantiation
    //==========================================================================
    
    hp_multiplier dut (
        .a         (a),
        .b         (b),
        .result    (result),
        .overflow  (overflow),
        .underflow (underflow),
        .nan_out   (nan_out)
    );
    
    //==========================================================================
    // Helper Functions
    //==========================================================================
    
    // Convert HP hex to real for display
    function real hex_to_real(input [15:0] hex_val);
        logic        sign;
        logic [4:0]  exp;
        logic [9:0]  mant;
        real         result;
        
        sign = hex_val[15];
        exp  = hex_val[14:10];
        mant = hex_val[9:0];
        
        if (exp == 5'h1F) begin
            if (mant == 0)
                result = sign ? -1.0/0.0 : 1.0/0.0;  // Infinity
            else
                result = 0.0/0.0;  // NaN
        end
        else if (exp == 0) begin
            if (mant == 0)
                result = 0.0;
            else
                result = (sign ? -1.0 : 1.0) * (real'(mant) / (2.0**10)) * (2.0**(-14));
        end
        else begin
            result = (sign ? -1.0 : 1.0) * (1.0 + real'(mant) / (2.0**10)) * (2.0**(exp - 15));
        end
        
        return result;
    endfunction
    
    //==========================================================================
    // Test Tasks
    //==========================================================================
    
    task automatic test_multiply(
        input [15:0] op_a,
        input [15:0] op_b,
        input [15:0] expected,
        input string test_name
    );
        a = op_a;
        b = op_b;
        
        #1;  // Small delay for combinational logic to settle
        
        $display("----------------------------------------");
        $display("Test: %s", test_name);
        $display("  A      = 0x%04h (%.6f)", op_a, hex_to_real(op_a));
        $display("  B      = 0x%04h (%.6f)", op_b, hex_to_real(op_b));
        $display("  Result = 0x%04h (%.6f)", result, hex_to_real(result));
        $display("  Expect = 0x%04h (%.6f)", expected, hex_to_real(expected));
        $display("  Flags  = overflow:%b underflow:%b nan:%b", overflow, underflow, nan_out);
        
        if (result == expected)
            $display("  STATUS: PASS");
        else
            $display("  STATUS: FAIL");
            
    endtask
    
    //==========================================================================
    // Main Test Sequence
    //==========================================================================
    
    initial begin
        $display("========================================");
        $display("HP Floating-Point Multiplier Testbench");
        $display("========================================");
        
        // Initialize
        a = 16'b0;
        b = 16'b0;
        
        #10;
        
        //----------------------------------------------------------------------
        // Test 1: 1.0 × 1.0 = 1.0
        // A = 0x3C00 (1.0)
        // B = 0x3C00 (1.0)
        // Expected = 0x3C00 (1.0)
        //----------------------------------------------------------------------
        test_multiply(
            16'h3C00,
            16'h3C00,
            16'h3C00,
            "1.0 x 1.0 = 1.0"
        );
        
        //----------------------------------------------------------------------
        // Test 2: 2.0 × 3.0 = 6.0
        // A = 0x4000 (2.0)
        // B = 0x4200 (3.0)
        // Expected = 0x4600 (6.0)
        //----------------------------------------------------------------------
        test_multiply(
            16'h4000,
            16'h4200,
            16'h4600,
            "2.0 x 3.0 = 6.0"
        );
        
        //----------------------------------------------------------------------
        // Test 3: -2.0 × 3.0 = -6.0
        // A = 0xC000 (-2.0)
        // B = 0x4200 (3.0)
        // Expected = 0xC600 (-6.0)
        //----------------------------------------------------------------------
        test_multiply(
            16'hC000,
            16'h4200,
            16'hC600,
            "-2.0 x 3.0 = -6.0"
        );
        
        //----------------------------------------------------------------------
        // Test 4: -2.0 × -3.0 = 6.0
        // A = 0xC000 (-2.0)
        // B = 0xC200 (-3.0)
        // Expected = 0x4600 (6.0)
        //----------------------------------------------------------------------
        test_multiply(
            16'hC000,
            16'hC200,
            16'h4600,
            "-2.0 x -3.0 = 6.0"
        );
        
        //----------------------------------------------------------------------
        // Test 5: 0.5 × 0.5 = 0.25
        // A = 0x3800 (0.5)
        // B = 0x3800 (0.5)
        // Expected = 0x3400 (0.25)
        //----------------------------------------------------------------------
        test_multiply(
            16'h3800,
            16'h3800,
            16'h3400,
            "0.5 x 0.5 = 0.25"
        );
        
        //----------------------------------------------------------------------
        // Test 6: 1.5 × 2.5 = 3.75
        // A = 0x3E00 (1.5)
        // B = 0x4100 (2.5)
        // Expected = 0x4380 (3.75)
        //----------------------------------------------------------------------
        test_multiply(
            16'h3E00,
            16'h4100,
            16'h4380,
            "1.5 x 2.5 = 3.75"
        );
        
        //----------------------------------------------------------------------
        // Test 7: Zero × Any = Zero
        // A = 0x0000 (0.0)
        // B = 0x4200 (3.0)
        // Expected = 0x0000 (0.0)
        //----------------------------------------------------------------------
        test_multiply(
            16'h0000,
            16'h4200,
            16'h0000,
            "0.0 x 3.0 = 0.0"
        );
        
        //----------------------------------------------------------------------
        // Test 8: Infinity × Finite = Infinity
        // A = 0x7C00 (+Inf)
        // B = 0x4000 (2.0)
        // Expected = 0x7C00 (+Inf)
        //----------------------------------------------------------------------
        test_multiply(
            16'h7C00,
            16'h4000,
            16'h7C00,
            "+Inf x 2.0 = +Inf"
        );
        
        //----------------------------------------------------------------------
        // Test 9: Infinity × Zero = NaN
        // A = 0x7C00 (+Inf)
        // B = 0x0000 (0.0)
        // Expected = 0x7E00 (NaN)
        //----------------------------------------------------------------------
        test_multiply(
            16'h7C00,
            16'h0000,
            16'h7E00,
            "+Inf x 0.0 = NaN"
        );
        
        //----------------------------------------------------------------------
        // Test 10: NaN × Any = NaN
        // A = 0x7E00 (NaN)
        // B = 0x4000 (2.0)
        // Expected = 0x7E00 (NaN)
        //----------------------------------------------------------------------
        test_multiply(
            16'h7E00,
            16'h4000,
            16'h7E00,
            "NaN x 2.0 = NaN"
        );
        
        //----------------------------------------------------------------------
        // Test 11: Overflow test
        // A = 0x7B00 (large)
        // B = 0x7B00 (large)
        // Expected = 0x7C00 (+Inf, overflow)
        //----------------------------------------------------------------------
        test_multiply(
            16'h7B00,
            16'h7B00,
            16'h7C00,
            "Large x Large = Overflow"
        );
        
        //----------------------------------------------------------------------
        // Test 12: Underflow test
        // A = 0x0400 (smallest normal)
        // B = 0x0400 (smallest normal)
        // Expected = 0x0000 (underflow)
        //----------------------------------------------------------------------
        test_multiply(
            16'h0400,
            16'h0400,
            16'h0000,
            "Tiny x Tiny = Underflow"
        );
        
        //----------------------------------------------------------------------
        // Summary
        //----------------------------------------------------------------------
        $display("========================================");
        $display("Testbench Complete");
        $display("========================================");
        
        #100;
        $finish;
    end

endmodule
