////////////////////////////////////////////////////////////////////////////////
// Testbench: tb_qp_fma
// Description: Testbench for IEEE 754 Quadruple-Precision Fused Multiply-Add
////////////////////////////////////////////////////////////////////////////////

`timescale 1ns / 1ps

module tb_qp_fma;

    //==========================================================================
    // Signals
    //==========================================================================
    
    logic [127:0] a, b, c;
    logic [127:0] result;
    logic         overflow;
    logic         underflow;
    logic         nan_out;
    
    //==========================================================================
    // DUT Instantiation
    //==========================================================================
    
    qp_fma dut (
        .a         (a),
        .b         (b),
        .c         (c),
        .result    (result),
        .overflow  (overflow),
        .underflow (underflow),
        .nan_out   (nan_out)
    );
    
    //==========================================================================
    // Helper Functions
    //==========================================================================
    
    function void display_qp(input [127:0] hex_val, input string name);
        logic         sign;
        logic [14:0]  exp;
        logic [111:0] mant;
        real          approx;
        
        sign = hex_val[127];
        exp  = hex_val[126:112];
        mant = hex_val[111:0];
        
        if (exp == 15'h7FFF) begin
            if (mant == 0)
                $display("  %s = 0x%032h (%sInfinity)", name, hex_val, sign ? "-" : "+");
            else
                $display("  %s = 0x%032h (NaN)", name, hex_val);
        end else if (exp == 0 && mant == 0) begin
            $display("  %s = 0x%032h (%s0.0)", name, hex_val, sign ? "-" : "+");
        end else begin
            approx = (sign ? -1.0 : 1.0) * (1.0 + real'(mant[111:80]) / (2.0**32)) * (2.0**(int'(exp) - 16383));
            $display("  %s = 0x%032h (approx: %e)", name, hex_val, approx);
        end
    endfunction
    
    //==========================================================================
    // Test Task
    //==========================================================================
    
    task automatic test_fma(
        input [127:0] op_a,
        input [127:0] op_b,
        input [127:0] op_c,
        input [127:0] expected,
        input string  test_name
    );
        a = op_a;
        b = op_b;
        c = op_c;
        
        #1;
        
        $display("----------------------------------------");
        $display("Test: %s", test_name);
        display_qp(op_a, "A     ");
        display_qp(op_b, "B     ");
        display_qp(op_c, "C     ");
        display_qp(result, "Result");
        display_qp(expected, "Expect");
        $display("  Flags  = overflow:%b underflow:%b nan:%b", overflow, underflow, nan_out);
        
        if (result == expected)
            $display("  STATUS: PASS");
        else
            $display("  STATUS: FAIL");
    endtask
    
    //==========================================================================
    // Main Test Sequence
    //==========================================================================
    
    initial begin
        $display("========================================");
        $display("QP Fused Multiply-Add Testbench");
        $display("========================================");
        
        a = 128'b0; b = 128'b0; c = 128'b0;
        #10;
        
        // Test 1: 1.0 × 1.0 + 0.0 = 1.0
        test_fma(128'h3FFF0000000000000000000000000000, 
                 128'h3FFF0000000000000000000000000000, 
                 128'h00000000000000000000000000000000, 
                 128'h3FFF0000000000000000000000000000, 
                 "1.0 x 1.0 + 0.0 = 1.0");
        
        // Test 2: 2.0 × 3.0 + 0.0 = 6.0
        test_fma(128'h40000000000000000000000000000000, 
                 128'h40008000000000000000000000000000, 
                 128'h00000000000000000000000000000000, 
                 128'h40018000000000000000000000000000, 
                 "2.0 x 3.0 + 0.0 = 6.0");
        
        // Test 3: 2.0 × 3.0 + 1.0 = 7.0
        test_fma(128'h40000000000000000000000000000000, 
                 128'h40008000000000000000000000000000, 
                 128'h3FFF0000000000000000000000000000, 
                 128'h401C0000000000000000000000000000, 
                 "2.0 x 3.0 + 1.0 = 7.0");
        
        // Test 4: 1.0 × 1.0 + 1.0 = 2.0
        test_fma(128'h3FFF0000000000000000000000000000, 
                 128'h3FFF0000000000000000000000000000, 
                 128'h3FFF0000000000000000000000000000, 
                 128'h40000000000000000000000000000000, 
                 "1.0 x 1.0 + 1.0 = 2.0");
        
        // Test 5: 2.0 × 2.0 + (-4.0) = 0.0 (exact cancellation)
        test_fma(128'h40000000000000000000000000000000, 
                 128'h40000000000000000000000000000000, 
                 128'hC0010000000000000000000000000000, 
                 128'h00000000000000000000000000000000, 
                 "2.0 x 2.0 + (-4.0) = 0.0");
        
        // Test 6: 0.5 × 0.5 + 0.5 = 0.75
        test_fma(128'h3FFE0000000000000000000000000000, 
                 128'h3FFE0000000000000000000000000000, 
                 128'h3FFE0000000000000000000000000000, 
                 128'h3FFE8000000000000000000000000000, 
                 "0.5 x 0.5 + 0.5 = 0.75");
        
        // Test 7: 4.0 × 4.0 + 0.0 = 16.0
        test_fma(128'h40010000000000000000000000000000, 
                 128'h40010000000000000000000000000000, 
                 128'h00000000000000000000000000000000, 
                 128'h40030000000000000000000000000000, 
                 "4.0 x 4.0 + 0.0 = 16.0");
        
        // Test 8: 0 × Any + C = C
        test_fma(128'h00000000000000000000000000000000, 
                 128'h40000000000000000000000000000000, 
                 128'h3FFF0000000000000000000000000000, 
                 128'h3FFF0000000000000000000000000000, 
                 "0.0 x 2.0 + 1.0 = 1.0");
        
        // Test 9: A × B + 0 = A × B
        test_fma(128'h40000000000000000000000000000000, 
                 128'h40008000000000000000000000000000, 
                 128'h00000000000000000000000000000000, 
                 128'h40018000000000000000000000000000, 
                 "2.0 x 3.0 + 0.0 = 6.0");
        
        // Test 10: Inf × finite + finite = Inf
        test_fma(128'h7FFF0000000000000000000000000000, 
                 128'h40000000000000000000000000000000, 
                 128'h3FFF0000000000000000000000000000, 
                 128'h7FFF0000000000000000000000000000, 
                 "Inf x 2.0 + 1.0 = Inf");
        
        // Test 11: Inf × 0 + finite = NaN
        test_fma(128'h7FFF0000000000000000000000000000, 
                 128'h00000000000000000000000000000000, 
                 128'h3FFF0000000000000000000000000000, 
                 128'h7FFF8000000000000000000000000000, 
                 "Inf x 0.0 + 1.0 = NaN");
        
        // Test 12: NaN propagation
        test_fma(128'h7FFF8000000000000000000000000000, 
                 128'h40000000000000000000000000000000, 
                 128'h3FFF0000000000000000000000000000, 
                 128'h7FFF8000000000000000000000000000, 
                 "NaN x 2.0 + 1.0 = NaN");
        
        // Test 13: Inf + (-Inf) = NaN
        test_fma(128'h7FFF0000000000000000000000000000, 
                 128'h3FFF0000000000000000000000000000, 
                 128'hFFFF0000000000000000000000000000, 
                 128'h7FFF8000000000000000000000000000, 
                 "Inf x 1.0 + (-Inf) = NaN");
        
        // Test 14: 1.5 × 1.5 + 0.0 = 2.25
        test_fma(128'h3FFF8000000000000000000000000000, 
                 128'h3FFF8000000000000000000000000000, 
                 128'h00000000000000000000000000000000, 
                 128'h40001000000000000000000000000000, 
                 "1.5 x 1.5 + 0.0 = 2.25");
        
        $display("========================================");
        $display("Testbench Complete");
        $display("========================================");
        
        #100;
        $finish;
    end

endmodule
