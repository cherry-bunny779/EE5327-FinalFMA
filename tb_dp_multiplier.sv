////////////////////////////////////////////////////////////////////////////////
// Testbench: tb_dp_multiplier
// Description: Testbench for IEEE 754 Double-Precision Floating-Point Multiplier
////////////////////////////////////////////////////////////////////////////////

`timescale 1ns / 1ps

module tb_dp_multiplier;

    //==========================================================================
    // Signals
    //==========================================================================
    
    logic [63:0] a;
    logic [63:0] b;
    logic [63:0] result;
    logic        overflow;
    logic        underflow;
    logic        nan_out;
    
    //==========================================================================
    // DUT Instantiation
    //==========================================================================
    
    dp_multiplier dut (
        .a         (a),
        .b         (b),
        .result    (result),
        .overflow  (overflow),
        .underflow (underflow),
        .nan_out   (nan_out)
    );
    
    //==========================================================================
    // Helper Functions
    //==========================================================================
    
    // Convert DP hex to real for display
    function real hex_to_real(input [63:0] hex_val);
        logic        sign;
        logic [10:0] exp;
        logic [51:0] mant;
        real         result;
        
        sign = hex_val[63];
        exp  = hex_val[62:52];
        mant = hex_val[51:0];
        
        if (exp == 11'h7FF) begin
            if (mant == 0)
                result = sign ? -1.0/0.0 : 1.0/0.0;  // Infinity
            else
                result = 0.0/0.0;  // NaN
        end
        else if (exp == 0) begin
            if (mant == 0)
                result = 0.0;
            else
                result = (sign ? -1.0 : 1.0) * (real'(mant) / (2.0**52)) * (2.0**(-1022));
        end
        else begin
            result = (sign ? -1.0 : 1.0) * (1.0 + real'(mant) / (2.0**52)) * (2.0**(exp - 1023));
        end
        
        return result;
    endfunction
    
    //==========================================================================
    // Test Tasks
    //==========================================================================
    
    task automatic test_multiply(
        input [63:0] op_a,
        input [63:0] op_b,
        input [63:0] expected,
        input string test_name
    );
        a = op_a;
        b = op_b;
        
        #1;  // Small delay for combinational logic to settle
        
        $display("----------------------------------------");
        $display("Test: %s", test_name);
        $display("  A      = 0x%016h (%.15e)", op_a, hex_to_real(op_a));
        $display("  B      = 0x%016h (%.15e)", op_b, hex_to_real(op_b));
        $display("  Result = 0x%016h (%.15e)", result, hex_to_real(result));
        $display("  Expect = 0x%016h (%.15e)", expected, hex_to_real(expected));
        $display("  Flags  = overflow:%b underflow:%b nan:%b", overflow, underflow, nan_out);
        
        if (result == expected)
            $display("  STATUS: PASS");
        else
            $display("  STATUS: FAIL");
            
    endtask
    
    //==========================================================================
    // Main Test Sequence
    //==========================================================================
    
    initial begin
        $display("========================================");
        $display("DP Floating-Point Multiplier Testbench");
        $display("========================================");
        
        // Initialize
        a = 64'b0;
        b = 64'b0;
        
        #10;
        
        //----------------------------------------------------------------------
        // Test 1: 1.0 × 1.0 = 1.0
        // A = 0x3FF0000000000000 (1.0)
        // B = 0x3FF0000000000000 (1.0)
        // Expected = 0x3FF0000000000000 (1.0)
        //----------------------------------------------------------------------
        test_multiply(
            64'h3FF0000000000000,
            64'h3FF0000000000000,
            64'h3FF0000000000000,
            "1.0 x 1.0 = 1.0"
        );
        
        //----------------------------------------------------------------------
        // Test 2: 2.0 × 3.0 = 6.0
        // A = 0x4000000000000000 (2.0)
        // B = 0x4008000000000000 (3.0)
        // Expected = 0x4018000000000000 (6.0)
        //----------------------------------------------------------------------
        test_multiply(
            64'h4000000000000000,
            64'h4008000000000000,
            64'h4018000000000000,
            "2.0 x 3.0 = 6.0"
        );
        
        //----------------------------------------------------------------------
        // Test 3: -2.0 × 3.0 = -6.0
        // A = 0xC000000000000000 (-2.0)
        // B = 0x4008000000000000 (3.0)
        // Expected = 0xC018000000000000 (-6.0)
        //----------------------------------------------------------------------
        test_multiply(
            64'hC000000000000000,
            64'h4008000000000000,
            64'hC018000000000000,
            "-2.0 x 3.0 = -6.0"
        );
        
        //----------------------------------------------------------------------
        // Test 4: -2.0 × -3.0 = 6.0
        // A = 0xC000000000000000 (-2.0)
        // B = 0xC008000000000000 (-3.0)
        // Expected = 0x4018000000000000 (6.0)
        //----------------------------------------------------------------------
        test_multiply(
            64'hC000000000000000,
            64'hC008000000000000,
            64'h4018000000000000,
            "-2.0 x -3.0 = 6.0"
        );
        
        //----------------------------------------------------------------------
        // Test 5: 0.5 × 0.5 = 0.25
        // A = 0x3FE0000000000000 (0.5)
        // B = 0x3FE0000000000000 (0.5)
        // Expected = 0x3FD0000000000000 (0.25)
        //----------------------------------------------------------------------
        test_multiply(
            64'h3FE0000000000000,
            64'h3FE0000000000000,
            64'h3FD0000000000000,
            "0.5 x 0.5 = 0.25"
        );
        
        //----------------------------------------------------------------------
        // Test 6: 5.75 × 3.25 = 18.6875
        // A = 0x4017000000000000 (5.75)
        // B = 0x400A000000000000 (3.25)
        // Expected = 0x4032B00000000000 (18.6875)
        //----------------------------------------------------------------------
        test_multiply(
            64'h4017000000000000,
            64'h400A000000000000,
            64'h4032B00000000000,
            "5.75 x 3.25 = 18.6875"
        );
        
        //----------------------------------------------------------------------
        // Test 7: Zero × Any = Zero
        // A = 0x0000000000000000 (0.0)
        // B = 0x4008000000000000 (3.0)
        // Expected = 0x0000000000000000 (0.0)
        //----------------------------------------------------------------------
        test_multiply(
            64'h0000000000000000,
            64'h4008000000000000,
            64'h0000000000000000,
            "0.0 x 3.0 = 0.0"
        );
        
        //----------------------------------------------------------------------
        // Test 8: Infinity × Finite = Infinity
        // A = 0x7FF0000000000000 (+Inf)
        // B = 0x4000000000000000 (2.0)
        // Expected = 0x7FF0000000000000 (+Inf)
        //----------------------------------------------------------------------
        test_multiply(
            64'h7FF0000000000000,
            64'h4000000000000000,
            64'h7FF0000000000000,
            "+Inf x 2.0 = +Inf"
        );
        
        //----------------------------------------------------------------------
        // Test 9: Infinity × Zero = NaN
        // A = 0x7FF0000000000000 (+Inf)
        // B = 0x0000000000000000 (0.0)
        // Expected = 0x7FF8000000000000 (NaN)
        //----------------------------------------------------------------------
        test_multiply(
            64'h7FF0000000000000,
            64'h0000000000000000,
            64'h7FF8000000000000,
            "+Inf x 0.0 = NaN"
        );
        
        //----------------------------------------------------------------------
        // Test 10: NaN × Any = NaN
        // A = 0x7FF8000000000000 (NaN)
        // B = 0x4000000000000000 (2.0)
        // Expected = 0x7FF8000000000000 (NaN)
        //----------------------------------------------------------------------
        test_multiply(
            64'h7FF8000000000000,
            64'h4000000000000000,
            64'h7FF8000000000000,
            "NaN x 2.0 = NaN"
        );
        
        //----------------------------------------------------------------------
        // Test 11: Overflow test
        // A = 0x7FE0000000000000 (large)
        // B = 0x7FE0000000000000 (large)
        // Expected = 0x7FF0000000000000 (+Inf, overflow)
        //----------------------------------------------------------------------
        test_multiply(
            64'h7FE0000000000000,
            64'h7FE0000000000000,
            64'h7FF0000000000000,
            "Large x Large = Overflow"
        );
        
        //----------------------------------------------------------------------
        // Test 12: Underflow test
        // A = 0x0010000000000000 (smallest normal)
        // B = 0x0010000000000000 (smallest normal)
        // Expected = 0x0000000000000000 (underflow)
        //----------------------------------------------------------------------
        test_multiply(
            64'h0010000000000000,
            64'h0010000000000000,
            64'h0000000000000000,
            "Tiny x Tiny = Underflow"
        );
        
        //----------------------------------------------------------------------
        // Test 13: Pi approximation × 2 ≈ 2*Pi
        // A = 0x400921FB54442D18 (3.14159265358979...)
        // B = 0x4000000000000000 (2.0)
        // Expected = 0x401921FB54442D18 (6.28318530717958...)
        //----------------------------------------------------------------------
        test_multiply(
            64'h400921FB54442D18,
            64'h4000000000000000,
            64'h401921FB54442D18,
            "Pi x 2.0 = 2*Pi"
        );
        
        //----------------------------------------------------------------------
        // Summary
        //----------------------------------------------------------------------
        $display("========================================");
        $display("Testbench Complete");
        $display("========================================");
        
        #100;
        $finish;
    end

endmodule
