////////////////////////////////////////////////////////////////////////////////
// Testbench: tb_sp_multiplier
// Description: Testbench for IEEE 754 Single-Precision Floating-Point Multiplier
////////////////////////////////////////////////////////////////////////////////

`timescale 1ns / 1ps

module tb_sp_multiplier;

    //==========================================================================
    // Signals
    //==========================================================================
    
    logic [31:0] a;
    logic [31:0] b;
    logic [31:0] result;
    logic        overflow;
    logic        underflow;
    logic        nan_out;
    
    //==========================================================================
    // DUT Instantiation
    //==========================================================================
    
    sp_multiplier dut (
        .a         (a),
        .b         (b),
        .result    (result),
        .overflow  (overflow),
        .underflow (underflow),
        .nan_out   (nan_out)
    );
    
    //==========================================================================
    // Helper Functions
    //==========================================================================
    
    // Convert SP hex to real for display
    function real hex_to_real(input [31:0] hex_val);
        logic        sign;
        logic [7:0]  exp;
        logic [22:0] mant;
        real         result;
        
        sign = hex_val[31];
        exp  = hex_val[30:23];
        mant = hex_val[22:0];
        
        if (exp == 8'hFF) begin
            if (mant == 0)
                result = sign ? -1.0/0.0 : 1.0/0.0;  // Infinity
            else
                result = 0.0/0.0;  // NaN
        end
        else if (exp == 0) begin
            if (mant == 0)
                result = 0.0;
            else
                result = (sign ? -1.0 : 1.0) * (real'(mant) / (2.0**23)) * (2.0**(-126));
        end
        else begin
            result = (sign ? -1.0 : 1.0) * (1.0 + real'(mant) / (2.0**23)) * (2.0**(exp - 127));
        end
        
        return result;
    endfunction
    
    //==========================================================================
    // Test Tasks
    //==========================================================================
    
    task automatic test_multiply(
        input [31:0] op_a,
        input [31:0] op_b,
        input [31:0] expected,
        input string test_name
    );
        a = op_a;
        b = op_b;
        
        #1;  // Small delay for combinational logic to settle
        
        $display("----------------------------------------");
        $display("Test: %s", test_name);
        $display("  A      = 0x%08h (%.10f)", op_a, hex_to_real(op_a));
        $display("  B      = 0x%08h (%.10f)", op_b, hex_to_real(op_b));
        $display("  Result = 0x%08h (%.10f)", result, hex_to_real(result));
        $display("  Expect = 0x%08h (%.10f)", expected, hex_to_real(expected));
        $display("  Flags  = overflow:%b underflow:%b nan:%b", overflow, underflow, nan_out);
        
        if (result == expected)
            $display("  STATUS: PASS");
        else
            $display("  STATUS: FAIL");
            
    endtask
    
    //==========================================================================
    // Main Test Sequence
    //==========================================================================
    
    initial begin
        $display("========================================");
        $display("SP Floating-Point Multiplier Testbench");
        $display("========================================");
        
        // Initialize
        a = 32'b0;
        b = 32'b0;
        
        #10;
        
        //----------------------------------------------------------------------
        // Test 1: 5.75 × 3.25 = 18.6875
        // A = 0x40B80000 (5.75)
        // B = 0x40500000 (3.25)
        // Expected = 0x41958000 (18.6875)
        //----------------------------------------------------------------------
        test_multiply(
            32'h40B80000,
            32'h40500000,
            32'h41958000,
            "5.75 x 3.25 = 18.6875"
        );
        
        //----------------------------------------------------------------------
        // Test 2: 1.0 × 1.0 = 1.0
        // A = 0x3F800000 (1.0)
        // B = 0x3F800000 (1.0)
        // Expected = 0x3F800000 (1.0)
        //----------------------------------------------------------------------
        test_multiply(
            32'h3F800000,
            32'h3F800000,
            32'h3F800000,
            "1.0 x 1.0 = 1.0"
        );
        
        //----------------------------------------------------------------------
        // Test 3: 2.0 × 3.0 = 6.0
        // A = 0x40000000 (2.0)
        // B = 0x40400000 (3.0)
        // Expected = 0x40C00000 (6.0)
        //----------------------------------------------------------------------
        test_multiply(
            32'h40000000,
            32'h40400000,
            32'h40C00000,
            "2.0 x 3.0 = 6.0"
        );
        
        //----------------------------------------------------------------------
        // Test 4: -2.0 × 3.0 = -6.0
        // A = 0xC0000000 (-2.0)
        // B = 0x40400000 (3.0)
        // Expected = 0xC0C00000 (-6.0)
        //----------------------------------------------------------------------
        test_multiply(
            32'hC0000000,
            32'h40400000,
            32'hC0C00000,
            "-2.0 x 3.0 = -6.0"
        );
        
        //----------------------------------------------------------------------
        // Test 5: -2.0 × -3.0 = 6.0
        // A = 0xC0000000 (-2.0)
        // B = 0xC0400000 (-3.0)
        // Expected = 0x40C00000 (6.0)
        //----------------------------------------------------------------------
        test_multiply(
            32'hC0000000,
            32'hC0400000,
            32'h40C00000,
            "-2.0 x -3.0 = 6.0"
        );
        
        //----------------------------------------------------------------------
        // Test 6: 0.5 × 0.5 = 0.25
        // A = 0x3F000000 (0.5)
        // B = 0x3F000000 (0.5)
        // Expected = 0x3E800000 (0.25)
        //----------------------------------------------------------------------
        test_multiply(
            32'h3F000000,
            32'h3F000000,
            32'h3E800000,
            "0.5 x 0.5 = 0.25"
        );
        
        //----------------------------------------------------------------------
        // Test 7: Zero × Any = Zero
        // A = 0x00000000 (0.0)
        // B = 0x40400000 (3.0)
        // Expected = 0x00000000 (0.0)
        //----------------------------------------------------------------------
        test_multiply(
            32'h00000000,
            32'h40400000,
            32'h00000000,
            "0.0 x 3.0 = 0.0"
        );
        
        //----------------------------------------------------------------------
        // Test 8: Infinity × Finite = Infinity
        // A = 0x7F800000 (+Inf)
        // B = 0x40000000 (2.0)
        // Expected = 0x7F800000 (+Inf)
        //----------------------------------------------------------------------
        test_multiply(
            32'h7F800000,
            32'h40000000,
            32'h7F800000,
            "+Inf x 2.0 = +Inf"
        );
        
        //----------------------------------------------------------------------
        // Test 9: Infinity × Zero = NaN
        // A = 0x7F800000 (+Inf)
        // B = 0x00000000 (0.0)
        // Expected = 0x7FC00000 (NaN)
        //----------------------------------------------------------------------
        test_multiply(
            32'h7F800000,
            32'h00000000,
            32'h7FC00000,
            "+Inf x 0.0 = NaN"
        );
        
        //----------------------------------------------------------------------
        // Test 10: NaN × Any = NaN
        // A = 0x7FC00000 (NaN)
        // B = 0x40000000 (2.0)
        // Expected = 0x7FC00000 (NaN)
        //----------------------------------------------------------------------
        test_multiply(
            32'h7FC00000,
            32'h40000000,
            32'h7FC00000,
            "NaN x 2.0 = NaN"
        );
        
        //----------------------------------------------------------------------
        // Test 11: Large number multiplication (overflow test)
        // A = 0x7E800000 (very large)
        // B = 0x7E800000 (very large)
        // Expected = 0x7F800000 (+Inf, overflow)
        //----------------------------------------------------------------------
        test_multiply(
            32'h7E800000,
            32'h7E800000,
            32'h7F800000,
            "Large x Large = Overflow"
        );
        
        //----------------------------------------------------------------------
        // Test 12: Small number multiplication (underflow test)
        // A = 0x00800000 (smallest normal)
        // B = 0x00800000 (smallest normal)
        // Expected = 0x00000000 (underflow to zero)
        //----------------------------------------------------------------------
        test_multiply(
            32'h00800000,
            32'h00800000,
            32'h00000000,
            "Tiny x Tiny = Underflow"
        );
        
        //----------------------------------------------------------------------
        // Summary
        //----------------------------------------------------------------------
        $display("========================================");
        $display("Testbench Complete");
        $display("========================================");
        
        #100;
        $finish;
    end

endmodule
