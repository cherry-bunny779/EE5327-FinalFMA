////////////////////////////////////////////////////////////////////////////////
// Testbench: tb_qp_multiplier
// Description: Testbench for IEEE 754 Quadruple-Precision Floating-Point Multiplier
////////////////////////////////////////////////////////////////////////////////

`timescale 1ns / 1ps

module tb_qp_multiplier;

    //==========================================================================
    // Signals
    //==========================================================================
    
    logic [127:0] a;
    logic [127:0] b;
    logic [127:0] result;
    logic         overflow;
    logic         underflow;
    logic         nan_out;
    
    //==========================================================================
    // DUT Instantiation
    //==========================================================================
    
    qp_multiplier dut (
        .a         (a),
        .b         (b),
        .result    (result),
        .overflow  (overflow),
        .underflow (underflow),
        .nan_out   (nan_out)
    );
    
    //==========================================================================
    // Helper Functions
    //==========================================================================
    
    // Display QP value (real conversion limited due to SystemVerilog constraints)
    function void display_qp(input [127:0] hex_val, input string name);
        logic         sign;
        logic [14:0]  exp;
        logic [111:0] mant;
        real          approx_value;
        
        sign = hex_val[127];
        exp  = hex_val[126:112];
        mant = hex_val[111:0];
        
        // Approximate display (limited precision)
        if (exp == 15'h7FFF) begin
            if (mant == 0)
                $display("  %s = 0x%032h (%sInfinity)", name, hex_val, sign ? "-" : "+");
            else
                $display("  %s = 0x%032h (NaN)", name, hex_val);
        end
        else if (exp == 0 && mant == 0) begin
            $display("  %s = 0x%032h (%s0.0)", name, hex_val, sign ? "-" : "+");
        end
        else begin
            // Rough approximation using top bits of mantissa
            approx_value = (sign ? -1.0 : 1.0) * (1.0 + real'(mant[111:80]) / (2.0**32)) * (2.0**(int'(exp) - 16383));
            $display("  %s = 0x%032h (approx: %e)", name, hex_val, approx_value);
        end
    endfunction
    
    //==========================================================================
    // Test Tasks
    //==========================================================================
    
    task automatic test_multiply(
        input [127:0] op_a,
        input [127:0] op_b,
        input [127:0] expected,
        input string  test_name
    );
        a = op_a;
        b = op_b;
        
        #1;  // Small delay for combinational logic to settle
        
        $display("----------------------------------------");
        $display("Test: %s", test_name);
        display_qp(op_a, "A     ");
        display_qp(op_b, "B     ");
        display_qp(result, "Result");
        display_qp(expected, "Expect");
        $display("  Flags  = overflow:%b underflow:%b nan:%b", overflow, underflow, nan_out);
        
        if (result == expected)
            $display("  STATUS: PASS");
        else
            $display("  STATUS: FAIL");
            
    endtask
    
    //==========================================================================
    // Main Test Sequence
    //==========================================================================
    
    initial begin
        $display("========================================");
        $display("QP Floating-Point Multiplier Testbench");
        $display("========================================");
        
        // Initialize
        a = 128'b0;
        b = 128'b0;
        
        #10;
        
        //----------------------------------------------------------------------
        // Test 1: 1.0 ū 1.0 = 1.0
        // A = 0x3FFF0000... (1.0)
        // B = 0x3FFF0000... (1.0)
        // Expected = 0x3FFF0000... (1.0)
        //----------------------------------------------------------------------
        test_multiply(
            128'h3FFF0000000000000000000000000000,
            128'h3FFF0000000000000000000000000000,
            128'h3FFF0000000000000000000000000000,
            "1.0 x 1.0 = 1.0"
        );
        
        //----------------------------------------------------------------------
        // Test 2: 2.0 ū 3.0 = 6.0
        // A = 0x40000000... (2.0)
        // B = 0x40008000... (3.0)
        // Expected = 0x40018000... (6.0)
        //----------------------------------------------------------------------
        test_multiply(
            128'h40000000000000000000000000000000,
            128'h40008000000000000000000000000000,
            128'h40018000000000000000000000000000,
            "2.0 x 3.0 = 6.0"
        );
        
        //----------------------------------------------------------------------
        // Test 3: -2.0 ū 3.0 = -6.0
        // A = 0xC0000000... (-2.0)
        // B = 0x40008000... (3.0)
        // Expected = 0xC0018000... (-6.0)
        //----------------------------------------------------------------------
        test_multiply(
            128'hC0000000000000000000000000000000,
            128'h40008000000000000000000000000000,
            128'hC0018000000000000000000000000000,
            "-2.0 x 3.0 = -6.0"
        );
        
        //----------------------------------------------------------------------
        // Test 4: -2.0 ū -3.0 = 6.0
        // A = 0xC0000000... (-2.0)
        // B = 0xC0008000... (-3.0)
        // Expected = 0x40018000... (6.0)
        //----------------------------------------------------------------------
        test_multiply(
            128'hC0000000000000000000000000000000,
            128'hC0008000000000000000000000000000,
            128'h40018000000000000000000000000000,
            "-2.0 x -3.0 = 6.0"
        );
        
        //----------------------------------------------------------------------
        // Test 5: 0.5 ū 0.5 = 0.25
        // A = 0x3FFE0000... (0.5)
        // B = 0x3FFE0000... (0.5)
        // Expected = 0x3FFD0000... (0.25)
        //----------------------------------------------------------------------
        test_multiply(
            128'h3FFE0000000000000000000000000000,
            128'h3FFE0000000000000000000000000000,
            128'h3FFD0000000000000000000000000000,
            "0.5 x 0.5 = 0.25"
        );
        
        //----------------------------------------------------------------------
        // Test 6: 4.0 ū 4.0 = 16.0
        // A = 0x40010000... (4.0)
        // B = 0x40010000... (4.0)
        // Expected = 0x40030000... (16.0)
        //----------------------------------------------------------------------
        test_multiply(
            128'h40010000000000000000000000000000,
            128'h40010000000000000000000000000000,
            128'h40030000000000000000000000000000,
            "4.0 x 4.0 = 16.0"
        );
        
        //----------------------------------------------------------------------
        // Test 7: Zero ū Any = Zero
        // A = 0x00000000... (0.0)
        // B = 0x40008000... (3.0)
        // Expected = 0x00000000... (0.0)
        //----------------------------------------------------------------------
        test_multiply(
            128'h00000000000000000000000000000000,
            128'h40008000000000000000000000000000,
            128'h00000000000000000000000000000000,
            "0.0 x 3.0 = 0.0"
        );
        
        //----------------------------------------------------------------------
        // Test 8: Infinity ū Finite = Infinity
        // A = 0x7FFF0000... (+Inf)
        // B = 0x40000000... (2.0)
        // Expected = 0x7FFF0000... (+Inf)
        //----------------------------------------------------------------------
        test_multiply(
            128'h7FFF0000000000000000000000000000,
            128'h40000000000000000000000000000000,
            128'h7FFF0000000000000000000000000000,
            "+Inf x 2.0 = +Inf"
        );
        
        //----------------------------------------------------------------------
        // Test 9: Infinity ū Zero = NaN
        // A = 0x7FFF0000... (+Inf)
        // B = 0x00000000... (0.0)
        // Expected = 0x7FFF8000... (NaN)
        //----------------------------------------------------------------------
        test_multiply(
            128'h7FFF0000000000000000000000000000,
            128'h00000000000000000000000000000000,
            128'h7FFF8000000000000000000000000000,
            "+Inf x 0.0 = NaN"
        );
        
        //----------------------------------------------------------------------
        // Test 10: NaN ū Any = NaN
        // A = 0x7FFF8000... (NaN)
        // B = 0x40000000... (2.0)
        // Expected = 0x7FFF8000... (NaN)
        //----------------------------------------------------------------------
        test_multiply(
            128'h7FFF8000000000000000000000000000,
            128'h40000000000000000000000000000000,
            128'h7FFF8000000000000000000000000000,
            "NaN x 2.0 = NaN"
        );
        
        //----------------------------------------------------------------------
        // Test 11: Overflow test
        // A = 0x7FFE0000... (large)
        // B = 0x7FFE0000... (large)
        // Expected = 0x7FFF0000... (+Inf, overflow)
        //----------------------------------------------------------------------
        test_multiply(
            128'h7FFE0000000000000000000000000000,
            128'h7FFE0000000000000000000000000000,
            128'h7FFF0000000000000000000000000000,
            "Large x Large = Overflow"
        );
        
        //----------------------------------------------------------------------
        // Test 12: Underflow test
        // A = 0x00010000... (smallest normal)
        // B = 0x00010000... (smallest normal)
        // Expected = 0x00000000... (underflow)
        //----------------------------------------------------------------------
        test_multiply(
            128'h00010000000000000000000000000000,
            128'h00010000000000000000000000000000,
            128'h00000000000000000000000000000000,
            "Tiny x Tiny = Underflow"
        );
        
        //----------------------------------------------------------------------
        // Test 13: -Infinity ū -Finite = +Infinity
        // A = 0xFFFF0000... (-Inf)
        // B = 0xC0000000... (-2.0)
        // Expected = 0x7FFF0000... (+Inf)
        //----------------------------------------------------------------------
        test_multiply(
            128'hFFFF0000000000000000000000000000,
            128'hC0000000000000000000000000000000,
            128'h7FFF0000000000000000000000000000,
            "-Inf x -2.0 = +Inf"
        );
        
        //----------------------------------------------------------------------
        // Test 14: 1.5 ū 1.5 = 2.25
        // 2.25 = 1.001? ū 2╣, mantissa = 0.001? = 1/8, bit 109 set = 0x2000...
        // A = 0x3FFF8000... (1.5)
        // B = 0x3FFF8000... (1.5)
        // Expected = 0x40002000... (2.25)
        //----------------------------------------------------------------------
        test_multiply(
            128'h3FFF8000000000000000000000000000,
            128'h3FFF8000000000000000000000000000,
            128'h40002000000000000000000000000000,
            "1.5 x 1.5 = 2.25"
        );
        
        //----------------------------------------------------------------------
        // Summary
        //----------------------------------------------------------------------
        $display("========================================");
        $display("Testbench Complete");
        $display("========================================");
        
        #100;
        $finish;
    end

endmodule
